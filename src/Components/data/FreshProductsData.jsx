// Helper functions
export const getProductById = (id) => {
  return products.find(product => product.id === id);
};

export const getProductsByCategory = (category) => {
  return category === "all" 
    ? products 
    : products.filter(product => product.category === category);
};

export const getFeaturedProducts = () => {
  return products.filter(product => product.discount > 20);
};

export const getOrganicProducts = () => {
  return products.filter(product => product.isOrganic === true);
};

export const getProductsBySubCategory = (subCategory) => {
  return products.filter(product => product.subCategory === subCategory);
};

export const getProductsByPriceRange = (min, max) => {
  return products.filter(product => product.price >= min && product.price <= max);
};

export const getInStockProducts = () => {
  return products.filter(product => product.inStock === true);
};

export const getProductsByOrigin = (origin) => {
  return products.filter(product => product.origin === origin);
};

export const getDiscountedProducts = () => {
  return products.filter(product => product.discount > 0);
};

export const getProductsByDiscountRange = (minDiscount, maxDiscount) => {
  return products.filter(product => product.discount >= minDiscount && product.discount <= maxDiscount);
};